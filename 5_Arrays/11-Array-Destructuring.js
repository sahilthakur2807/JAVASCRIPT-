// Array Destructuring
// Array destructuring lets you unpack values from an array into separate variables.

const colors = ["red", "green", "blue"];
const [firstColor, , thirdColor] = colors;

console.log(firstColor);
console.log(thirdColor);


//bus hum array ke first aur third element ko alag variable me store kar rahe hain, second element ko ignore kar rahe hain.
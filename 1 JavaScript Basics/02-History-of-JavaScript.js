// History of JavaScript
// JavaScript was created by Brendan Eich in 1995 at Netscape in about 10 days.
// It was later standardized as ECMAScript so browsers could follow common language rules.

// Important milestones:
// - 1995: JavaScript created
// - 1997: ECMAScript standard introduced
// - 2015: ES6 brought modern syntax such as let, const, classes, and template literals

// Why this history matters:
// Older code often uses outdated patterns, while modern code follows newer standards.

const originalName = "JavaScript";
const standardName = "ECMAScript";

console.log(`${originalName} is implemented through the ${standardName} standard.`);

// Example of older style versus modern style:
var oldStyle = "legacy syntax";
const modernStyle = "preferred syntax";
console.log(oldStyle);
